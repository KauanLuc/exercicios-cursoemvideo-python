expressao = str(input('Digite sua expressao: '))
pilha = []
for simbolo in expressao:
    if simbolo == '(':
        pilha.append('(')
    elif simbolo == ')':
        if len(pilha) > 0:
            pilha.pop()
        else:
            pilha.append(')')
            break
if len(pilha) == 0:
    print('Sua express√£o esta correta.')
else:
    print('Sua expressao nao esta correta.')